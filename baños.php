<h3>DIVICIONES DE BAÑO</h3>
</center>
<h4>
<br/>
La línea de divisiones de baño es bastante amplia puesto que está sujeta a las necesidades del cliente, principalmente se manejan tres tipos de divisiones: batiente, corrediza y en ele, cada una de estas en aluminio, bronce cromado o acero inoxidable, adicionalmente dan un acabado moderno a las nuevas construcciones o remodelaciones.
<br/><br/>

Nuestras divisiones de baño ofrecen resistencia a las condiciones de humedad y más posibilidades de combinación de materiales, lo que genera variedad de colores y superficies.
